public class Condominio {
    private int id;
    private int Napartamento;
    private String placaC;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNapartamento() {
        return Napartamento;
    }

    public void setNapartamento(int napartamento) {
        Napartamento = napartamento;
    }

    public String getPlacaC() {
        return placaC;
    }

    public void setPlacaC(String placaC) {
        this.placaC = placaC;
    }
}
